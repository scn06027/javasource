package basic;

//정수형- 1. byte
//		2. short
//		3. int(가장 중요한 타입) : 4byte 사용
//		4. long : 8byte
public class IntVariable {
	public static void main(String[] args) {
		
		int i=10;
		System.out.println("i= "+i);
		
		int var1; //변수 선언
		var1=75;
		System.out.println("var1= "+var1);
		
		long var2= 12345678900L;
		//뒤에 l이라는 문자를 붙이면 오류가 안난다.
	}

}
